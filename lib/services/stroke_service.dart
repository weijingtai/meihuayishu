  /// 获取单个字的笔画数
  int getStrokeCount(String character) {
    if (character.isEmpty) return 0;
    final count = strokeData[character];
    if (count == null) {
      _logger.w('未找到字 "$character" 的笔画数据');
      return 0;
    }
    return count;
  }

  /// 获取多个字的笔画总数
  int getTotalStrokeCount(String text) {
    int total = 0;
    for (var char in text.split('')) {
      total += getStrokeCount(char);
    }
    return total;
  }

  /// 获取字符串中每个字的笔画数列表
  List<int> getStrokeCounts(String text) {
    return text.split('').map((char) => getStrokeCount(char)).toList();
  }

  /// 检查是否所有字都有笔画数据
  bool hasAllStrokes(String text) {
    return text.split('').every((char) => strokeData.containsKey(char));
  }

  /// 获取缺失笔画数据的字列表
  List<String> getMissingCharacters(String text) {
    return text
        .split('')
        .where((char) => !strokeData.containsKey(char))
        .toList();
  }
